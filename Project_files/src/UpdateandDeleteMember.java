    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        int checkid=0;
        String id=txtid.getText();
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from member where id='"+id+"'");
            while(rs.next()){
                checkid=1;
                txtid.setEditable(false);
                txtname.setText(rs.getString(2));
                txtage.setText(rs.getString(3));
                txtemail.setText(rs.getString(4));
                txtnumber.setText(rs.getString(5));
                txtcnic.setText(rs.getString(6));
                txtcity.setText(rs.getString(7));
                
            }
            if(checkid==0){
                JOptionPane.showMessageDialog(null, "Member ID does not exist");
            }
        }catch(Exception e)
        {
            JOptionpane.showMessage(null, e);
        }
    }//GEN-LAST:event_btnsearchActionPerformed
